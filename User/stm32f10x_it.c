// SysTick櫓뙤륩蛟변鑒，첼1ms쏵흙寧늴，첼100ms零貫FLAG_100MS
void SysTick_Handler(void)
{
    static uint8_t tick_cnt = 0;
    delay_tick_inc();
    if (++tick_cnt >= 100U)
    {
        tick_cnt = 0;
        FLAG_100MS = 1;
    }
}
